public class Fermata {

    private final String luogo;
    private final String orario;

    public Fermata(String luogo, String orario) {
        this.luogo = luogo;
        this.orario = orario;
    }

    public String getLuogo() {
        return luogo;
    }
    public String getOrario() {
        return orario;
    }
}
